using System;
using System.Collections.Generic;
using System.Text;

namespace Ui.Enums
{
    /// <summary>
    /// Possible options for button shapes.
    /// </summary>
    public enum ButtonShape
    {
        /// <summary>
        /// Indicates that the button will be of a square shape.
        /// </summary>
        Square,

        /// <summary>
        /// Indicates that the button will be of a circle shape.
        /// </summary>
        Circular,

        /// <summary>
        /// Indicates that the button will be of a rectangle shape.
        /// </summary>
        Rectangular,

        /// <summary>
        /// Indicates that the button will be of a pill shape.
        /// </summary>
        Pill,
    }
}
