using System;
using System.Collections.Generic;
using System.Text;

namespace Ui.Enums
{
    /// <summary>
    /// Indicates the alignment of an object.
    /// </summary>
    public enum Alignment
    {
        /// <summary>
        /// Indicates that the object is aligned to the top of it's container.
        /// </summary>
        Top,

        /// <summary>
        /// Indicates that the object is aligned to the bottom of it's container.
        /// </summary>
        Bottom,

        /// <summary>
        /// Indicates that the object is aligned to the left side of it's container.
        /// </summary>
        Left,

        /// <summary>
        /// Indicates that the object is aligned to the right side of it's container.
        /// </summary>
        Right,

        /// <summary>
        /// Indicates that the object is hidden.
        /// </summary>
        Hidden,
    }
}
